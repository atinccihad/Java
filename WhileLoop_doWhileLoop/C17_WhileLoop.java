package day18_19_whileLoop_doWhileLoop;

public class C17_WhileLoop {

	public static void main(String[] args) {
		// 20 den 30'a kadar olan sayilarin 20 ve 30 dahil
		// while loop ile yazdirin
		
		for (int i = 20; i <=30; i++) {
			System.out.print(i+" ");
		}

		System.out.println("");
		// for loop kismi
		
		
		int sayi=20;
		
		while(sayi<=30) {
			System.out.println(sayi+" ");
			sayi++;
		}
		
		// while loop kismi
		
		
		// for loop ile cozebildigimiz sorularda while loop kullanmak
		// kodumuzu biraz uzatir
		 
	}

}
